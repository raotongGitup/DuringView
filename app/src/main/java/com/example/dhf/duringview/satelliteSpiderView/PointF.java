public class PointF {
    public String x;
    public float y;

    public PointF(String x, float y) {
        this.x = x;
        this.y = y;
    }

    public PointF() {
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
